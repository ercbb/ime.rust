/// 判断是否是中文字符
fn is_chinese_char(c: &str) -> bool {
    c.chars().next().map_or(false, |ch| {
        ('\u{4e00}'..='\u{9fff}').contains(&ch)
            || ('\u{3400}'..='\u{4dbf}').contains(&ch)
            || ('\u{20000}'..='\u{2a6df}').contains(&ch)
    })
}

